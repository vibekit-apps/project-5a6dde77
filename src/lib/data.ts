export interface DrinkingData {
  dailyData: { [date: string]: number };
  monthlyData: { month: string; drinks: number }[];
  totalDrinks: number;
  averagePerMonth: number;
  highestMonth: { month: string; drinks: number };
  currentStreak: number;
}

export function processData(): DrinkingData {
  // Parse CSV data
  const dailyData: { [date: string]: number } = {};
  const lines = rawData.trim().split('\n');
  
  lines.forEach(line => {
    const [dateStr, countStr] = line.split(',');
    dailyData[dateStr] = parseInt(countStr, 10);
  });

  // Calculate total drinks
  const totalDrinks = Object.values(dailyData).reduce((sum, drinks) => sum + drinks, 0);

  // Group by month
  const monthlyTotals: { [month: string]: number } = {};
  Object.entries(dailyData).forEach(([dateStr, drinks]) => {
    const date = parseISO(dateStr);
    const monthKey = format(date, 'yyyy-MM');
    monthlyTotals[monthKey] = (monthlyTotals[monthKey] || 0) + drinks;
  });

  // Convert to array and sort
  const monthlyData = Object.entries(monthlyTotals)
    .map(([month, drinks]) => ({
      month: format(parseISO(month + '-01'), 'MMM yyyy'),
      drinks
    }))
    .sort((a, b) => a.month.localeCompare(b.month));

  // Calculate average per month
  const averagePerMonth = totalDrinks / monthlyData.length;

  // Find highest month
  const highestMonth = monthlyData.reduce((max, current) => 
    current.drinks > max.drinks ? current : max
  );

  // Calculate current streak of zero-drink days
  const today = startOfToday();
  const sortedDates = Object.keys(dailyData)
    .sort((a, b) => b.localeCompare(a)) // Sort descending
    .filter(dateStr => {
      const date = parseISO(dateStr);
      return !isAfter(date, today); // Only include dates up to today
    });

  let currentStreak = 0;
  for (const dateStr of sortedDates) {
    if (dailyData[dateStr] === 0) {
      currentStreak++;
    } else {
      break;
    }
  }

  return {
    dailyData,
    monthlyData,
    totalDrinks,
    averagePerMonth,
    highestMonth,
    currentStreak
  };
}